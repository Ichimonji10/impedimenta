#============================================================================-*\
# Description
#============================================================================-*/
# This program simply reads a file from STDIN and echoes it back out to STDOUT.

#============================================================================-*\
# Global Declarations
#============================================================================-*/

#============================================================================-*\
# Subroutines
#============================================================================-*/
sub Main {
    my @copy_of_file = <STDIN>;
    my $line = "";

    foreach $line (@copy_of_file) {
        print STDOUT "$line";
    }
}

#============================================================================-*\
# Main
#============================================================================-*/
Main();
