#include "CharNode.h"

namespace JeremysCode {
    /*-========================================================================/
    CharNode(CharNode*, char = ' ');
    /========================================================================-*/
    CharNode::CharNode(CharNode* nextNode, char newCh) 
        : link(nextNode), ch(newCh)
    {
        // intentionally empty
    }

    /*-========================================================================/
    CharNode* getLink() const;
    /========================================================================-*/
    CharNode* CharNode::getLink() const {
        return link;
    }

    /*-========================================================================/
    char getCh() const
    /========================================================================-*/
    char CharNode::getCh() const {
        return ch;
    }

    /*-========================================================================/
    void setLink(CharNode*)
    /========================================================================-*/
    void CharNode::setLink(CharNode* newLink) {
        link = newLink;
    }

    /*-========================================================================/
    void setCh(char)
    /========================================================================-*/
    void CharNode::setCh(char newCh) {
        ch = newCh;
    }
}
